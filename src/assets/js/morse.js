module.exports = {
  toMorse: function(english) {
    english = english.toLowerCase();
    var str = "";
    
    for (let i = 0; i < english.length; i++) {
      let c = english[i];
      // If the character has a mapping, apply the mapping.
      if (etom[c] !== undefined) {
        str += etom[c] + ' ';
      } else {
        str += c;
      }
    }
  
    return str;
  },
  
  toEnglish: function(morse) {
    var str = "";
    
    morse.split(' ').forEach(s => {
      if (mtoe[s] !== undefined) {
        str += mtoe[s];
      } else {
        str += s;
      }
    });
    return str;
  }
}